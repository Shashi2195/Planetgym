// Function to populate districts based on selected state
function populateDistricts() {
    const stateSelect = document.querySelector('select[name="state"]');
    const districtSelect = document.querySelector('select[name="district"]');
    const selectedState = stateSelect.value;

    // Clear existing options
    districtSelect.innerHTML = '<option value="">Select District</option>';

    // Populate districts if state is selected
    if (selectedState && districtData[selectedState]) {
        districtData[selectedState].forEach(district => {
            const option = document.createElement('option');
            option.value = district.toLowerCase().replace(/\s+/g, '-');
            option.textContent = district;
            districtSelect.appendChild(option);
        });
    }
}

// Add event listener to state dropdown
document.addEventListener('DOMContentLoaded', function () {
    const stateSelect = document.querySelector('select[name="state"]');
    if (stateSelect) {
        stateSelect.addEventListener('change', populateDistricts);
    }
});

// Mobile menu toggle
document.addEventListener('DOMContentLoaded', function () {
    const menuToggle = document.querySelector('.menu-toggle');
    const navMenu = document.querySelector('nav ul');
    const body = document.body;

    if (menuToggle && navMenu) {
        menuToggle.addEventListener('click', function () {
            const isOpen = navMenu.classList.toggle('show');
            menuToggle.classList.toggle('active');

            // Change hamburger icon to X when menu is open
            menuToggle.textContent = isOpen ? '✕' : '☰';

            // Prevent body scroll when menu is open
            body.style.overflow = isOpen ? 'hidden' : '';
        });

        // Close menu when clicking on a link
        navMenu.querySelectorAll('a').forEach(link => {
            link.addEventListener('click', function () {
                navMenu.classList.remove('show');
                menuToggle.classList.remove('active');
                menuToggle.textContent = '☰';
                body.style.overflow = '';
            });
        });

        // Close menu when clicking outside
        document.addEventListener('click', function (event) {
            if (!navMenu.contains(event.target) && !menuToggle.contains(event.target)) {
                navMenu.classList.remove('show');
                menuToggle.classList.remove('active');
                menuToggle.textContent = '☰';
                body.style.overflow = '';
            }
        });

        // Close menu on window resize if screen becomes larger
        window.addEventListener('resize', function () {
            if (window.innerWidth > 767) {
                navMenu.classList.remove('show');
                menuToggle.classList.remove('active');
                menuToggle.textContent = '☰';
                body.style.overflow = '';
            }
        });

        // Close menu on escape key
        document.addEventListener('keydown', function (event) {
            if (event.key === 'Escape' && navMenu.classList.contains('show')) {
                navMenu.classList.remove('show');
                menuToggle.classList.remove('active');
                menuToggle.textContent = '☰';
                body.style.overflow = '';
            }
        });
    }
});

// Smooth scrolling for navigation links
document.querySelectorAll('a[href^="#"]').forEach(anchor => {
    anchor.addEventListener('click', function (e) {
        e.preventDefault();
        const target = document.querySelector(this.getAttribute('href'));
        if (target) {
            target.scrollIntoView({
                behavior: 'smooth',
                block: 'start'
            });
        }
    });
});

